    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        try{
            if (comments.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Invalid Input. Please fill in the comments");
            }else {
                String rating_input = (starrating.getSelectedItem().toString());
                String comment = comments.getText();
                String new_id = Integer.toString(DataIO.createNewFeedbackID());
                Customer customer = Car_Rental_System.loginUser;

                Feedback t = new Feedback(new_id,rating_input,comment,customer);
                allFeedbacks.add(t);
                DataIO.writeToTextFeedbackFile();
                JOptionPane.showMessageDialog(null,"Feedback submited successfully.\n"
                + "Thank you for helping us to get better.");
                dispose();
            }
            
  
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Invalid Input. Enter Digit 1-5 only");
        }
        
    }//GEN-LAST:event_submitActionPerformed

